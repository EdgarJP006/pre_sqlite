/// A class representing an Employee with a first name and a last name.
///
/// The [Employee] class contains two properties:
/// - [FirstName]: The first name of the employee.
/// - [LastName]: The last name of the employee.
///
/// The constructor initializes both properties.
class Employee {
  String FirstName;
  String LastName;

  Employee(this.FirstName, this.LastName);
}
